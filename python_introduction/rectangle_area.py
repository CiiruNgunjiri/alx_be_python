# python script that calculates the area of a rectangle
# define the variables in the script and assign values
# the area is calculated by multiplying its length by its width

# length is the value representing the length of the rectangle
# width is the value representing the width of the rectangle 

length = 10
width = 5

# we calculate the area using the formular (length * width)

area = length * width

# print the calculated area in the format: 'The area of the rectangle is:[area]'

print (f"The area of the rectangle is: {area}")
